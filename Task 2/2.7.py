# Declaring variables of different data types
integer_value = 25          # Integer
float_value = 3.1415        # Float
string_value = "Hello, Python!"  # String
bool_value = True           # Boolean

# Printing the values and their types
print("Integer Value:", integer_value, "| Type:", type(integer_value))
print("Float Value:", float_value, "| Type:", type(float_value))
print("String Value:", string_value, "| Type:", type(string_value))
print("Boolean Value:", bool_value, "| Type:", type(bool_value))
